package com.syntax.class90917Arrays;

public class prakskc {

	public static void main(String[] args) {

		
		/*
		 *  \' -> Single quotation mark
            \" -> Double quotation mark
            \\ -> Backslash \
            \t -> TAb
            \b -> Backspace
            \r -> Carriage return
            \f -> Formfeed
            \n -> Newline 
		 */
		
		String b="Bob\nAnna";
		
		System.out.println(b);
		
	}
}